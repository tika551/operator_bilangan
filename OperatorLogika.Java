public class operatorLogika { public static void main(String[] args) {

    //contoh : operator AND (&&) dan OR (||)

    boolean x = true;

    boolean y = false;

    

    boolean and = x && y;

    boolean or = x || y;



    System.out.println("hasil AND:" + and);

    System.out.println("hasil OR:" + or);

    

}

    

}
